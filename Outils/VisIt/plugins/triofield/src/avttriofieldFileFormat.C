  const int nnodes = field_._nbnodes;
  const int dim3 =field_._space_dim == 3;
  points->SetNumberOfPoints(nnodes);
  float* pts = (float *) points->GetVoidPointer(0);
  int jl=0;
  int i;
  for (i = 0; i < nnodes; i++) {
    pts[jl]   = pos(i,0);
    pts[jl+1] = pos(i,1);
    pts[jl+2] = dim3 ? pos(i,2) : 0.;
    jl+=3;
  }
